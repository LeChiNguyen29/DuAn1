    public SanPhamModel getDataFormSP() {
        boolean isValid = true;
        SanPhamModel sp = null;
        String maSanPham = this.txtMaSanPham.getText().trim();
        if (maSanPham.isEmpty()) {
            this.errMaSP.setText("Không bỏ trống dữ liệu");
            isValid = false;
        } else {
            this.errMaSP.setText(null);
        }
        String ten = this.txtTenSP.getText().trim();
        if (ten.isEmpty()) {
            this.errTenSP.setText("Không bỏ trống dữ liệu");
            isValid = false;
        } else {
            this.errTenSP.setText(null);
        }
        String trString = (String) this.cbbTrangThaiSP.getSelectedItem();
        int trangThai;
        if (trString.equals("Đang bán")) {
            trangThai = 0;
        } else {
            trangThai = 1;
        }
        if (isValid) {
            sp = new SanPhamModel(maSanPham, Timestamp.valueOf(LocalDateTime.now()), Timestamp.valueOf(LocalDateTime.now()), ten, trangThai);
        }
        return sp;
    }

