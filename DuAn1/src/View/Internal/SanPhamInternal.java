    private void searchSPCT() {
        String ten = this.txtSearch.getText().trim();
        SanPhamModel spModel = null;
        for (SanPhamModel sp : this.sanPhamService.searchTenSanPham(ten)) {
            if (ten.equalsIgnoreCase(sp.getTenSanPham())) {
                System.out.println("true");
                spModel = sp;
                break;
            }
        }
        String selectedItem = (String) this.cbbSearchTrangThai.getSelectedItem();
        Integer trangThai = null;
        if (selectedItem.equals("Tất Cả")) {
            trangThai = null;
        } else if (selectedItem.equals("Đang Bán")) {
            trangThai = 1;
        } else {
            trangThai = 0;
        }

        DanhMucModel danhMuc = null;
        if (this.cbbSearchDanhMuc.getSelectedIndex() != 0) {
            danhMuc = (DanhMucModel) this.cbbSearchDanhMuc.getSelectedItem();
        }
        try {
            this.loadTableSPCT(chiTietService.search(trangThai, danhMuc, spModel));
            chiTietService.search(trangThai, danhMuc, spModel).forEach(x -> {
                System.out.println(x);
            });
        } catch (SQLException ex) {
        }
    }

