    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed

        int row = this.tblGiay.getSelectedRow();
        if (row == -1) {

            diaLogThongBao.thongBao("Chọn dữ liệu để sửa!");
        } else {
            SanPhamChiTietModel sp = this.getFormSPCT();
            if (sp == null) {
                return;
            } else {
                int id = (int) this.tblGiay.getValueAt(row, 6);
                if (chiTietService.isMaMauUnique(id, sp.getMaSanPhamCt())) {
                    this.lblErrMa.setText("Mã đã tồn tại");
                    return;
                } else {
                    DiaLogYesNo logYesNo = new DiaLogYesNo(null, "Bạn có muốn cập nhật dữ liệu không?");
                    logYesNo.setVisible(true);

                    if (logYesNo.getResult() == JOptionPane.YES_OPTION) {
                        if (chiTietService.updateSPCT(id, sp)) {
                          
                            this.clearFormSPCT();
                            diaLogThongBao.thongBao("Cập nhật dữ liệu thành công!");
                        } else {
                            diaLogThongBao.thongBao("Cập nhật dữ liệu thất bại!");
                        }
                    }

                }

            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

