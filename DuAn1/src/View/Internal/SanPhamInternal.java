    public SanPhamChiTietModel getFormSPCT() {
        SanPhamChiTietModel spct = null;
        boolean isValid = true;
        String ma = this.txtMa.getText().trim();
        if (ma.isEmpty()) {
            this.lblErrMa.setText("Không để trống mã");
            isValid = false;
        } else {
            this.lblErrMa.setText(null);
        }
        SanPhamModel sanPhamModel = (SanPhamModel) this.cbbTenSanPham.getSelectedItem();
        DanhMucModel danhMucModel = (DanhMucModel) this.cbbDanhMuc.getSelectedItem();
        ChatLieuModel chatLieuModel = (ChatLieuModel) this.cbbChatLieu.getSelectedItem();
        KichCoModel kichCoModel = (KichCoModel) this.cbbKichCo.getSelectedItem();
        MauSacModel mauSacModel = (MauSacModel) this.cbbMauSac.getSelectedItem();
        NhaSanXuatModel nhaSanXuatModel = (NhaSanXuatModel) this.cbbNhaSanXuat.getSelectedItem();

        String donGiaStr = this.txtDonGia.getText().trim();
        if (donGiaStr.isEmpty()) {
            this.lblErrDonGia.setText("Không để trống đơn giá");
            isValid = false;
        } else if (!donGiaStr.matches("^(?!0(\\.0+)?$)(\\d*\\.?\\d+)$")) {
            this.lblErrDonGia.setText("Đơn giá không đúng định dạng");
            isValid = false;
        } else {
            this.lblErrDonGia.setText(null);
        }
        String soLuongStr = this.txtSoLuong.getText().trim();
        if (soLuongStr.isEmpty()) {
            this.lblErrSoLuong.setText("Không để trống số lượng");
            isValid = false;
        } else if (!soLuongStr.matches("^[1-9]\\d*$")) {
            this.lblErrSoLuong.setText("Số lượng không đúng định dạng");
            isValid = false;
        } else {
            this.lblErrSoLuong.setText(null);
        }

        String moTa = this.txtMoTa.getText().trim();
        String trangThaiStr = (String) this.cbbTrangThai.getSelectedItem();
        int trangThai;
        if (trangThaiStr.equals("Đang bán")) {
            trangThai = 0;
        } else {
            trangThai = 1;
        }
        String urlImage = url;
        if (urlImage == null) {
            this.lblErrImg.setText("Thêm ảnh sản phẩm");
            isValid = false;
        }
        if (!isValid) {
            return null;
        }
        int soLuong = Integer.parseInt(soLuongStr);

        BigDecimal donGIa = BigDecimal.valueOf(Double.parseDouble(donGiaStr));
        spct = new SanPhamChiTietModel(urlImage, donGIa, ma, moTa, soLuong, trangThai, chatLieuModel, danhMucModel, kichCoModel, mauSacModel, nhaSanXuatModel, sanPhamModel, Timestamp.valueOf(LocalDateTime.now()), Timestamp.valueOf(LocalDateTime.now()));

        return spct;
    }

