    private void btnSuaSanPhamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaSanPhamActionPerformed

        int row = this.tblSanPham.getSelectedRow();
        if (row < 0) {

            diaLogThongBao.thongBao("Chọn dữ liệu để sửa");
        } else {
            SanPhamModel sp = this.getDataFormSP();
            if (sp == null) {
                return;
            } else {
                int id = Integer.parseInt(String.valueOf(this.tblSanPham.getValueAt(row, 3)));
                if (sanPhamService.isMaUnique(id, sp.getMaSanPham())) {
                    this.errMaSP.setText("Mã sản phẩm đã tồn tại");
                    return;
                } else {
                    DiaLogYesNo logYesNo = new DiaLogYesNo(null, "Bạn có muốn cập nhật dữ liệu không?");
                    logYesNo.setVisible(true);

                    if (logYesNo.getResult() == JOptionPane.YES_OPTION) {
                        if (sanPhamService.updateById(id, sp)) {
                            this.loadTableSanPham(sanPhamService.getAllSanPham());
                            diaLogThongBao.thongBao("Cập nhật dữ liệu thành công!");
                        }
                    }

                }
            }
        }
    }//GEN-LAST:event_btnSuaSanPhamActionPerformed

