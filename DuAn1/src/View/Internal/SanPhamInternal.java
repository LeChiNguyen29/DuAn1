    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked
        int row = this.tblSanPham.getSelectedRow();
        if (row == -1) {
            return;
        } else {
            this.txtMaSanPham.setText((String) this.tblSanPham.getValueAt(row, 0));
            this.txtTenSP.setText((String) this.tblSanPham.getValueAt(row, 1));
            if (this.tblSanPham.getValueAt(row, 2).toString().equals("Đang bán")) {
                this.cbbTrangThaiSP.setSelectedIndex(0);
            } else {
                this.cbbTrangThaiSP.setSelectedIndex(1);
            }
        }
        this.clearErrSanPham();
    }//GEN-LAST:event_tblSanPhamMouseClicked

    private void btnThemSanPhamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemSanPhamActionPerformed
        SanPhamModel sp = this.getDataFormSP();
        if (sp == null) {
            return;
        } else {
            if (sanPhamService.checkMaSP(sp.getMaSanPham())) {

                diaLogThongBao.thongBao("Mã sản phẩm " + sp.getMaSanPham() + " đã tồn tại");
                return;
            } else {
                if (sanPhamService.createSanPham(sp)) {
                   
                    this.clearFormSanPham();

                    diaLogThongBao.thongBao("Thêm mới sản phẩm thành công");

                } else {

                    diaLogThongBao.thongBao("Thêm mới sản phẩm thất bại");

                }
            }
        }


    }//GEN-LAST:event_btnThemSanPhamActionPerformed

